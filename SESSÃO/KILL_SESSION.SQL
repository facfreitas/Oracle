SELECT COUNT(*)
FROM V$SESSION
WHERE TERMINAL LIKE 'VIA%' 
AND STATUS != 'KILLED'

SELECT COUNT(*), TERMINAL
FROM V$SESSION
HAVING COUNT(*) > 1
GROUP BY TERMINAL

